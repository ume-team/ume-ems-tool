
/* EM_TASK_MGR : 定时任务管理表 */
DROP TABLE IF EXISTS `EM_TASK_MGR` ;
CREATE TABLE `EM_TASK_MGR` (
    `TASK_ID` VARCHAR(8) BINARY NOT NULL	 COMMENT 'JOB ID',
    `TASK_NAME` VARCHAR(64) BINARY 	 COMMENT 'JOB名称',
    `TASK_ENABLE` INT(1) 	DEFAULT 0 COMMENT 'JOB是否可用标志',
    `TASK_STATUS` VARCHAR(64) BINARY 	 COMMENT 'JOB运行状态',
    `CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者',
    `CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者',
    `UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT '定时任务管理表' ;
ALTER TABLE `EM_TASK_MGR` ADD CONSTRAINT `PK_EM_TASK_MGR` PRIMARY KEY ( TASK_ID ) ;


/* EM_ENTITY_CFG : EM实体展示配置表 */
DROP TABLE IF EXISTS `EM_ENTITY_CFG` ;
CREATE TABLE `EM_ENTITY_CFG` (
    `ID` VARCHAR(64) BINARY NOT NULL	 COMMENT '实体ID',
    `CONTENT` TEXT 	 COMMENT '实体Config',
    `CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者',
    `CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者',
    `UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT 'EM实体展示配置表' ;
ALTER TABLE `EM_ENTITY_CFG` ADD CONSTRAINT `PK_EM_ENTITY_CFG` PRIMARY KEY ( ID ) ;
